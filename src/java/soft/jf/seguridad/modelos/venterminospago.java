public class venterminospago {
    private String idterminopago;
    private String terminopago;
    private int diasvencimiento;
    private int generapagares;
    private int numeropagares;
    private int generapagaresautomatico;

    public String getIdterminopago() {
        return idterminopago;
    }

    public void setIdterminopago(String idterminopago) {
        this.idterminopago = idterminopago;
    }

    public String getTerminopago() {
        return terminopago;
    }

    public void setTerminopago(String terminopago) {
        this.terminopago = terminopago;
    }

    public int getDiasvencimiento() {
        return diasvencimiento;
    }

    public void setDiasvencimiento(int diasvencimiento) {
        this.diasvencimiento = diasvencimiento;
    }

    public int getGenerapagares() {
        return generapagares;
    }

    public void setGenerapagares(int generapagares) {
        this.generapagares = generapagares;
    }

    public int getNumeropagares() {
        return numeropagares;
    }

    public void setNumeropagares(int numeropagares) {
        this.numeropagares = numeropagares;
    }

    public int getGenerapagaresautomatico() {
        return generapagaresautomatico;
    }

    public void setGenerapagaresautomatico(int generapagaresautomatico) {
        this.generapagaresautomatico = generapagaresautomatico;
    }
    
    

}
